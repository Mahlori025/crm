export default function Sidebar() {
  const { data: session } = useSession();
  const pathname = usePathname();
  const [isCollapsed, setIsCollapsed] = useState(false);

  const navigation: NavItem[] = [
    {
      name: 'Dashboard',
      href: '/dashboard',
      icon: DashboardIcon,
    },
    {
      name: 'Tickets',
      href: '/tickets',
      icon: TicketsIcon,
      permission: Permission.VIEW_OWN_TICKETS,
    },
    {
      name: 'Agents',
      href: '/agents',
      icon: UsersIcon,
      permission: Permission.VIEW_USERS,
      roles: [Role.ADMIN, Role.MANAGER],
    },
    {
      name: 'Reports',
      href: '/reports',
      icon: ReportsIcon,
      permission: Permission.VIEW_REPORTS,
      roles: [Role.ADMIN, Role.MANAGER],
    },
    {
      name: 'Settings',
      href: '/dashboard/settings',
      icon: SettingsIcon,
      permission: Permission.MANAGE_SETTINGS,
      roles: [Role.ADMIN],
    },
  ];

  const filteredNavigation = navigation.filter(item => {
    if (!session) return true; // Show all items for unauthenticated users in dev mode
    
    // Check role-based access
    if (item.roles && !item.roles.includes(session.user.role)) {
      return false;
    }
    
    // Check permission-based access
    if (item.permission && !hasPermission(session.user.role, item.permission)) {
      return false;
    }
    
    return true;
  });

  return (
    <div className={`bg-gray-900 text-white transition-all duration-300 ${
      isCollapsed ? 'w-16' : 'w-64'
    } min-h-screen flex flex-col`}>
      {/* Header */}
      <div className="p-4 border-b border-gray-800">
        <div className="flex items-center justify-between">
          {!isCollapsed && (
            <h1 className="text-xl font-bold">Gibela Portal</h1>
          )}
          <button
            onClick={() => setIsCollapsed(!isCollapsed)}
            className="p-2 rounded-lg hover:bg-gray-800 transition-colors"
          >
            <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} 
                d={isCollapsed ? "M9 5l7 7-7 7" : "M15 19l-7-7 7-7"} />
            </svg>
          </button>
        </div>
      </div>

      {/* Navigation */}
      <nav className="flex-1 p-4 space-y-2">
        {filteredNavigation.map((item) => {
          const isActive = pathname === item.href || pathname.startsWith(item.href + '/');
          
          return (
            <Link
              key={item.name}
              href={item.href}
              className={`flex items-center px-3 py-2 rounded-lg text-sm font-medium transition-colors ${
                isActive
                  ? 'bg-blue-600 text-white'
                  : 'text-gray-300 hover:bg-gray-800 hover:text-white'
              }`}
            >
              <item.icon className="w-6 h-6 flex-shrink-0" />
              {!isCollapsed && (
                <span className="ml-3">{item.name}</span>
              )}
              {!isCollapsed && item.badge && (
                <span className="ml-auto bg-red-500 text-white text-xs rounded-full px-2 py-1">
                  {item.badge}
                </span>
              )}
            </Link>
          );
        })}
      </nav>

      {/* User Info */}
      {session && (
        <div className="p-4 border-t border-gray-800">
          <div className="flex items-center">
            <div className="w-8 h-8 rounded-full bg-blue-600 flex items-center justify-center text-white font-medium">
              {session.user.name?.charAt(0) || 'U'}
            </div>
            {!isCollapsed && (
              <div className="ml-3">
                <p className="text-sm font-medium">{session.user.name}</p>
                <p className="text-xs text-gray-400">{session.user.role}</p>
              </div>
            )}
          </div>
        </div>
      )}
    </div>
  );
}